IIT-buildingThe Informatics Institute of Technology (IIT) was established in 1990 as the first private higher education institute that awards reputed British degrees in the field of Information Communication Technology (ICT) and Business.

Over the course of two decades IIT has not only established itself as a pioneer in education but has also produced many successful entrepreneurs and engineers that have contributed to both local and international corporate and government entities. Up to date, IIT has produced over 3000 graduates that have annexed themselves in eminent positions in many companies in Sri Lanka and abroad.</p>

<hr style="border-color: #292c2f">
<h3 id="head1">Offerings</h3>

        <p id= "details1">

IIT offers BEng (Hons) Software Engineering and BSc (Hons) Information Systems with Business Management (ISBM) internal degrees from the University of Westminster, UK. Several entry paths to the degree programmes such as the Foundation Programme in Higher Education and the Preliminary Certificate in Computing (PCC) are also offered. IIT also offers an MSc Advanced Software Engineering from the University of Westminster.

Innovative thinking has always been a core principle of the institute and this principle is passed down to its students by encouraging them to nurture creativity and critical thinking. By empowering education within Sri Lanka, IIT aims to produce an exponential growth in the number of IT and business professionals within the country.</p>
<hr style="border-color: #292c2f">
<h3 id="head2">Industrial Placement</h3>
<p id= "details2">
Initially the institute was focused on providing British higher education to the youth of Sri Lanka; this goal however expanded to provide students with a more hands-on experience by giving students a chance to obtain work experience during the course of their degree. This comes as a mandatory one year industrial placement which will not only empower a student with only a degree but also with the necessary work experience. With this added advantage, IIT students have a 100% employment assurance and is most often selected for high income employment at renowned organizations.

Many international and local companies have come forward to provide the necessary exposure for IIT students which have helped them to gain an insight into their respective industries as well as acquire essential skills that were vital to a successful career.

Most IIT alumni are employed in premier mercantile companies including John Keells, Unilever, Aitken Spence, IBM, Hatton National Bank, Informatics, Millenium IT, Fonterra, Zone 24 x 7, WSO2, Brandix, Industrial and Financial Systems (IFS), Commercial Bank, NDB, Vanik, Virtusa etc. Some have even ventured into setting up their own business. IIT graduates are also employed in the UK, Germany, USA, Singapore, Middle-East, Australia and the Maldives. Whilst some are continuing their postgraduate studies up to PhD level others have completed their PhD’s and are holding varied appointments.</p>
<hr style="border-color: #292c2f">
<h3 id="head3">An award winning institution</h3>
<p id= "details3">
The rich academic tradition maintained by IIT has contributed immensely to the institute’s success rate of the students passing out. Currently IIT, which has matured fast, has evolved into an academic institution recognized by many for its academic quality and producing rounded students.

The proof of IIT’s superiority lies in the number of local and international awards and accolades its students have received over a period of time. In many of these competitions state run universities have been major stakeholders. IIT has won several awards which include the National Best Quality Software Awards (NBQSA) which was won in three consecutive years. This award was won by competing with all universities in the island. IIT has also won other awards which include Asia Pacific Information Communication Technology Alliances (APICTA) Awards, Google summer of code and e-Swabhimani. In 2013, a team consisting of IIT students became the first Sri Lankan team to receive the Samsung Digital Native Award at the Microsoft World Imagine Cup finals held at St. Petersburg, Russia. Continuing the winning streak, IIT was victorious once again at the 2014 Microsoft Imagine Cup competition. Five teams from IIT participated in the ‘World Citizenship’, ‘Innovation’ and ‘Games’ categories and all five teams went on to win awards at the Microsoft Imagine Cup Sri Lanka finals, bringing credit to IIT.</p>
