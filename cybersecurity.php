			     <p>A quick Internet search will reveal many good sites where you can learn about Internet (cyber) safety. Look for .org and .gov sites, which tend to provide more objective information; others may be promoting a particular product.<br>
<br>
These are some key items to keep in mind as you develop your own cyber-security plan:<br>
<br>
1.  Safety applies to all computer users - adults and children alike.<br>
2.  Kids should be taught never to post personal info or engage with strangers online.<br>
3. Even if children aren’t looking for inappropriate sites, it’s very easy to be exposed to them by accident. This can happen at friends’ homes or in links friends share. Establish clear rules about using the Internet, as well as open lines of communication about what your children do and experience online.<br>
4.  Time spent online should be limited, with simple guidelines that the whole family follows together.<br>
5.  Always be sure that you’re on the right website, especially on financial websites. Look at the URL in your browser bar—it should begin with “https://” to indicate the website is secure, and the website address should be clear and straightforward. If the URL seems wrong, stop and double-check before sharing any login or personal information.<br>
6.  Consider keeping computers in a common space in your home, so family members won’t spend hours surfing the web alone, in a separate room.</p>
			 
